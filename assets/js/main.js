const my_number_1 = 10;
const my_number_2 = 20;



function sum_number(number_1, number_2) {
    return number_1 + number_2
}
const sum_total = sum_number(my_number_1, my_number_2);
console.log(sum_total)



function div_number(number_1, number_2) {
    return number_1 / number_2
}
const div_total = div_number(my_number_1, my_number_2);
console.log(div_total)



function mult_number(number_1, number_2) {
    return number_1 * number_2
}
const mult_total = mult_number(my_number_1, my_number_2);
console.log(mult_total)
